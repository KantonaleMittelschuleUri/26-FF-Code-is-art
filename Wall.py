class Wall:

    xDim = 23
    yDim = 23
    square_size = 31

    def checkForWall(x,y):
        #if 0 <= x <= 22 and 0 <= y <= 22:
        x = x % 23
        y = y % 23
        return wallmap[y][x] == 1

    def paint(screen):
        for index_i, i in enumerate(wallmap):
            for index_j, j in enumerate(i):
                if Wall.checkForWall(index_j,index_i):
                    pygame.draw.rect(screen, "blue", (index_j * Wall.square_size, index_i * Wall.square_size, Wall.square_size, Wall.square_size))
